/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_USART2_UART_Init();
  MX_DAC1_Init();
  MX_TIM4_Init();
  /* USER CODE BEGIN 2 */

  HAL_TIM_Base_Start(&htim4);

  uint8_t *p = (uint8_t *)&__USER_DATA;
  p += 4; // "RIFF"
  p += 4; // samples
  p += 4; // "WAVE"
  p += 4; // "fmt "
  p += 4; // fmtチャンクのバイト数
  p += 2; // 音声フォーマット
  p += 2; // チャンネル数 (1を想定
  p += 4; // サンプリング周波数(Hz)
  p += 4; // 1 秒あたりバイト数の平均 (サンプリング周波数 * ブロックサイズ)
  p += 2; // ブロックサイズ
  p += 2; // ビット／サンプル
  p += 4; // "data"

  // サブチャンクサイズ  波形データの「バイト数」
  uint32_t samples = *p + (*(p+1) * 256)  + (*(p+2) * 256 * 256)  + (*(p+3) * 256 * 256 * 256);
  samples /= 2;
  p += 4;

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	  // ボタンが押されていたら実行
	  if (HAL_GPIO_ReadPin(B1_GPIO_Port, B1_Pin) == GPIO_PIN_RESET) {
		  // beep
		  int16_t *pp = (int16_t *)p;
		  uint16_t dmaBuf[2][BUFFERSIZE];
		  int bank = 0;

		  flg_dma_done = 1;
		  int count = samples;
		  while (0 < count) {
			  const int blksize = MIN(samples, BUFFERSIZE);

			  // 波形を DAC出力に合わせる (振幅とオフセット)
			  for(int i=0; i< blksize; i++) {
				  int16_t v0 = *pp;
				  pp++;
				  int32_t v = v0;
				  v >>= 4;
				  v += 2047;

				  dmaBuf[bank][i] = v & 0x0fff;
			  }

			  // wait for DMA complete
			  while(!flg_dma_done) {
				  __NOP();
			  }

			  flg_dma_done = 0;
			  HAL_DAC_Start_DMA(&hdac1, DAC_CHANNEL_1, (uint32_t*)dmaBuf[bank], blksize, DAC_ALIGN_12B_R);

			  bank = !bank;
			  count -= blksize;
		  }

		  HAL_DAC_Stop_DMA(&hdac1, DAC_CHANNEL_1);
	  }



    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

